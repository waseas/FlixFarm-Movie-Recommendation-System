    public void generateRequestFile(String date) throws IOException{
        try {
            file = new File(REQUEST_FILE_PATH);
            if(file.exists()){
                file.delete();
            }
            file.createNewFile();
            System.out.println("New Request File Created");
            writer = new FileWriter(file);
        
            writer.append(REQUEST_HEADER);
            writer.append(LINE_BREAK);
            
            
           String column = hospitalOrganization.getName()+","+date+","+ txtNeededThroatSwab.getText() +","+ txtNeededNasopharyngealSwab.getText()  +","+ txtNeededPCRMachine.getText()  +","+ txtNeededECGMachine.getText();
           writer.append(column);
           writer.append(LINE_BREAK); 
            
        }finally{
            try {
                writer.flush();
                writer.close();
            } catch (IOException e) {
                System.out.println("Error while flushing/closing fileWriter !!!");
                e.printStackTrace();
            }
        }
        
        
        
    }
    
