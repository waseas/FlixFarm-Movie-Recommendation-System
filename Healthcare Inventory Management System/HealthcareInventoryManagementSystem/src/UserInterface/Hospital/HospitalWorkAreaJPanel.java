            String host = "smtp.gmail.com";
            String user = "klkeshav08@gmail.com";
            String pass = "Alpha_123";
           
            Properties properties = System.getProperties();  
            properties.setProperty("mail.smtp.host", host);
            properties.put("mail.smtp.starttls.required", "true");
            properties.put("mail.smtp.starttls.enable", "true");
            properties.put("mail.smtp.host",host);  
            properties.put("mail.smtp.port", "587");  
            properties.put("mail.smtp.auth", "true");  
            java.security.Security.addProvider(new com.sun.net.ssl.internal.ssl.Provider());  
            Session session;
            session = Session.getDefaultInstance(properties,  null);
            session.setDebug(sessionDebug);
           
            try{
            MimeMessage message = new MimeMessage(session);  
            message.setFrom(new InternetAddress(from));  
            message.setRecipient(Message.RecipientType.TO,new InternetAddress(to));  
            InternetAddress address;
            address = new InternetAddress(to);
            message.setSubject("CDC System Alert - New Sales Request");
            
            
            BodyPart messageBodyPart = new MimeBodyPart();
            messageBodyPart.setText("Please find the attachment for request details.");
            Multipart multipart = new MimeMultipart();
            multipart.addBodyPart(messageBodyPart);
            
             messageBodyPart = new MimeBodyPart();
             
            String filename = REQUEST_FILE_PATH;
            //generateRequestFile(date);
            DataSource source = new FileDataSource(filename);
            messageBodyPart.setDataHandler(new DataHandler(source));
            messageBodyPart.setFileName(filename);
            multipart.addBodyPart(messageBodyPart);
            message.setContent(multipart);
            
            Transport transport = session.getTransport("smtp");
            transport.connect(host,user,pass);
            transport.sendMessage(message, message.getAllRecipients());
            transport.close();
            }
            catch(Exception e){
                System.out.println(e);
                JOptionPane.showMessageDialog(null,"Error in raising new request");
            }
